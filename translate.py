def translate_file(filepath):
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    original = content
    
    # First apply comment translations (exact line matches, longer first)
    sorted_comments = sorted(comment_translations.items(), key=lambda x: len(x[0]), reverse=True)
    for chinese, english in sorted_comments:
        content = content.replace(chinese, english)
    
    # Then apply UI string translations (longer first to avoid partial matches)
    sorted_translations = sorted(translations.items(), key=lambda x: len(x[0]), reverse=True)
    for chinese, english in sorted_translations:
        content = content.replace(chinese, english)
    
    if content != original:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)
        return True
    return False

