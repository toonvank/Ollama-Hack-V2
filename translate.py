count = 0
for root, dirs, files in os.walk('frontend/src'):
    for f in files:
        if f.endswith(('.tsx', '.ts')):
            path = os.path.join(root, f)
            if translate_file(path):
                count += 1
                print(f'Translated: {path}')

print(f'\nTotal files translated: {count}')
